//PORT PB4 and PB5 OUTPUTS
void Leds_Init()
{
	unsigned long volatile delay;
	
	SYSCTL_RCGC2_R |= 0x00000002; // activate port B
  delay = SYSCTL_RCGC2_R;    // allow time to finish activating
  GPIO_PORTB_AMSEL_R &= ~0x3F;      // no analog 
  GPIO_PORTB_PCTL_R &= ~0x00000FFF; // regular function
  GPIO_PORTB_DIR_R |= 0x3F;      // make PB4 & PB5 out
  GPIO_PORTB_AFSEL_R &= ~0x3F;   // disable alt funct on PB4 & PB5
  GPIO_PORTB_DEN_R |= 0x3F;      // enable digital I/O on PB4 & PB5
	
	GPIO_PORTB_DATA_R &= 0x00;			//start with LEDs off
		
}

//PORTS PE1 and PE2 They are edge interrupts
void Switches_Init()
{
	unsigned long volatile delay;
	
	SYSCTL_RCGC2_R |= 0x00000010; // activate port E
  delay = SYSCTL_RCGC2_R;    // allow time to finish activating
	
	GPIO_PORTE_AMSEL_R &= ~0x03;      // no analog 
  GPIO_PORTE_PCTL_R &= ~0x000000FF; // regular function
  GPIO_PORTE_DIR_R &= ~0x03;      // make PE1 & PE0 in
  GPIO_PORTE_AFSEL_R &= ~0x03;   // disable alt funct on PE1 & PE0 
  GPIO_PORTE_DEN_R |= 0x03;      // enable digital I/O on PE1 & PE0 
	
	
	GPIO_PORTE_IS_R &= ~0x03;     // (d) PE1 & PE2 is edge-sensitive
  GPIO_PORTE_IBE_R &= ~0x03;    //     PE1 & PE2 is not both edges
  GPIO_PORTE_IEV_R |= 0x03;    //     PE1 & PE2 RISING edge event
  GPIO_PORTE_ICR_R = 0x03;      // (e) clear flag4
  GPIO_PORTE_IM_R |= 0x03;      // (f) arm interrupt on PE1 & PE2
	
	
  NVIC_PRI1_R = (NVIC_PRI1_R & 0xFFFFFF10)|0x000000A0; // (g) priority 4
  NVIC_EN0_R = 0x00000010;      // (h) enable interrupt 4 in NVIC
  EnableInterrupts();           // (i) Enable global Interrupt flag (I)
	
}
