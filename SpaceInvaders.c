int DetectCollision(Figure One, Figure Two)
{
	//int Collision = 0;
	int top1, bottom1, left1, right1, top2, bottom2, left2, right2;

	top1 = One.y - One.image[0][22] / 2;
	bottom1 = One.y;
	left1 = One.x;
	right1 = One.x + One.image[0][18] / 2;
	
	top2 = Two.y - Two.image[0][22] / 2;
	bottom2 = Two.y;
	left2 = Two.x;
	right2 = Two.x + Two.image[0][18] / 2;
	
	if ( bottom1 < top2 )		//Figure One is above Two
		return 0;
	if ( right1 < left2 )		//Figure One is to the left of figure Two
		return 0;
	if ( left1 > right2 ) 	//Figure One is to the right of Two
		return 0;
	if ( top1 > bottom2 )		// Figure One is bellow Two
		return 0;

//		return 0;
	
	return 1;
}
























